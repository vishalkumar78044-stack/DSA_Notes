package Arre;

public class Arre_01 {
    public static void main(String[] args) {
        // Arr is mutabile
        int[] x = {6,19,9,5,7,67};
        //indexing
        System.out.println(x[0]);
        // updating elements
        System.out.println(x[3]);
        x[3] = 89;
        System.out.println(x[3]);
    }
}
